# operadores aritméticos
# SUMA

print(f'Resultado de SUMA: {2 + 3}')  # resultado: 5

# RESTA
print(f'Resultado de RESTA: {5 - 2}')  # resultado: 3

# MULTIPLICACIÓN
print(f'Resultado de MULTIPLICACIÓN: {4 * 3}')  # resultado: 12

# DIVISIÓN
print(f'Resultado de DIVISIÓN: {10 / 2}')  # resultado: 5.0
print(f'Resultado de DIVISIÓN: {8 / 3}')   # resultado: 2.6666666666666665

# DIVISIÓN ENTERA
print(f'Resultado de DIVISIÓN ENTERA: {10 // 2}')  # resultado: 5
print(f'Resultado de DIVISIÓN ENTERA: {8 // 3}')   # resultado: 2

# MÓDULO
print(f'Resultado de MÓDULO: {8 % 3}')  # resultado: 2

# POTENCIA
print(f'Resultado de POTENCIA: {2 ** 3}')  # resultado: 8


# Operadores Logicos
# AND
print(f'Resultado de AND: {True and False}')  # resultado: False
print(f'Resultado de AND: {3 > 2 and 5 < 7}')   # resultado: True

# OR
print(f'Resultado de OR: {True or False}')  # resultado: True
print(f'Resultado de OR: {3 > 2 or 5 > 7}')   # resultado: True

# NOT
print(f'Resultado de NOT: {not True}')  # resultado: False
print(f'Resultado de NOT: {not (3 > 2 or 5 > 7)}')   # resultado: False

# Operadores de comparación
# IGUALDAD
print(f'Resultado de IGUALDAD: {5.0 == 5}')  # resultado: True
print(f'Resultado de IGUALDAD: {type(5.0) == type(5)}')  # resultado: False

# DESIGUALDAD
print(f'Resultado de DESIGUALDAD: {5 != 3}')  # resultado: True

# MAYOR QUE
print(f'Resultado de MAYOR QUE: {5 > 3}')  # resultado: True    

# MENOR QUE
print(f'Resultado de MENOR QUE: {5 < 3}')  # resultado: False

# MAYOR O IGUAL QUE
print(f'Resultado de MAYOR O IGUAL QUE: {5 >= 5}')  # resultado: True   

# MENOR O IGUAL QUE
print(f'Resultado de MENOR O IGUAL QUE: {5 <= 3}')  # resultado: False  
